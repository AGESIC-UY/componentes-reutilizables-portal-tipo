										<p class="Page-description">La arquitectura de la información se centra en la organización, clasificación, y etiquetado de los contenidos, con el objetivo de facilitar a los usuarios el acceso a la información.</p>
										
										<p>La arquitectura se estructura, generalmente, en el menú de navegación del sitio, ubicado en el cabezal. En los sitios institucionales de gub.uy, el menú está estandarizado en la <a href="#">Arquitectura Portal institucional (.xlsx 20kb)</a> y sus categorías principales ya definidas: Institucional, Políticas y gestión, Trámites y servicios, Datos y estadísticas y Comunicación. A su vez, las categorías Institucional y Trámites y servicios tienen definidas sus subcategorías, mientras que para Políticas y gestión, Datos y estadísticas y Comunicación cada organismo puede y debe nombrar, definir y organizar su contenido.</p>
										
										<p>La arquitectura de la información para todos los organismos que pertenecen a gub.uy es única y asegura que el modelo de interacción sea el mismo para todos los sitios, consolidando la imagen del Estado y utilizando una única manera de relacionarse con los ciudadanos.</p>
									
