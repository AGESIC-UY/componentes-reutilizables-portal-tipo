                  <h2 class="Page-title">Datos Abiertos</h2>
                  <h3 id="1-qu-debe-contener-sta-secci-n-">1. ¿Qué debe contener ésta sección?</h3>
                  <p>Debe contener información acerca de todos los datos abiertos que la institución haya publicado.</p>
                  <h3 id="2-rea-de-datos-abiertos">2. Área de Datos Abiertos</h3>
                  <p>Incluir una sección en el portal Institucional denominada Datos Abiertos desde la cual sea posible acceder a los conjuntos de datos abiertos publicados por el organismo.</p>
                  <p>Es aceptable que en dicha sección estén los archivos a descargar o estén los enlaces a los archivos que se encuentran en otra sección del portal. Por ejemplo, puede existir una sección "Estadísticas" y que los archivos se encuentren allí y en la sección "Datos Abiertos " simplemente se encuentren los enlaces a dichos archivos. También es aceptable una combinación de las 2 formas presentadas: archivos para descargar y enlaces a archivos para descargar.</p>
                  <p>Además se recomienda incluir un enlace a los términos de uso publicados en el <a href="http://catalogodatos.gub.uy/">Catálogo de Datos Abiertos</a> indicando su adherencia.</p>
                  <h3 id="3-datos-gub-uy-http-datos-gub-uy-">3. <a href="http://datos.gub.uy">datos.gub.uy</a></h3>
                  <p>Catalogar en <a href="http://datos.gub.uy">datos.gub.uy</a> los datos que se encuentran o referencian en la sección Datos Abiertos del portal institucional (indicada en el ítem 1).</p>
                  <h3 id="4-3-estrellas">4. 3 estrellas</h3>
                  <p>Los datos abiertos estén al menos en nivel “3 estrellas” , en formatos CSV, XML y JSON.</p>
                  <p>Sin embargo, la prioridad es publicar los datos por lo que si se cuenta con datos en otro formato procesable automáticamente como por ejemplo XLS, es aceptable publicarlos.</p>
                  <h3 id="5-metadatos">5. Metadatos</h3>
                  <p>Siempre que se publique un conjunto de datos abiertos se debe publicar un archivo de metadatos asociado. Este archivo describe los atributos de los datos con al menos los siguientes metadatos:</p>
                  <ul>
                    <li><strong>Nombre del atributo</strong></li>
                    <li><strong>Descripción:</strong> significado del atributo</li>
                    <li><strong>Tipo de dato:</strong> nombre del atributo</li>
                    <li><strong>Recurso relacionado:</strong> nombre del atributo</li>
                    <li><strong>Información adicional:</strong> que ayude a interpretar el dato</li>
                  </ul>
                  <h3 id="6-licencia">6. Licencia</h3>
                  <p>Los conjuntos de datos deberán tener una licencia asociada. Se recomienda utilizar la licencia de datos abiertos de Uruguay.</p>
                  <h3 id="7-qu-datos-abrir-">7. ¿Qué datos abrir?</h3>
                  <ul>
                    <li>Los que ya están disponibles en otros formatos</li>
                    <li>Los que se dan a demanda</li>
                    <li>Los que son fácil de generar</li>
                    <li>Los que tengan aplicaciones asociadas</li>
                    <li>Todos los informes publicados deben tener publicado el conjunto de datos utilizado para realizarlo</li>
                  </ul>
