                <div class="Page-document">
                  <h2 class="Page-title">Participación</h2>
                  <h3 id="1-qu-debe-contener-sta-secci-n-">1. ¿Qué debe contener ésta sección?</h3>
                  <p>En esta sección se deberán presentar todos los medios que la institución brinda al usuario para participar así como el histórico de las instancias de participación y sus resultados.</p>
                  <p>Promover la participación implica informar a los ciudadanos y hacerlos partícipes de las decisiones del organismo.</p>
                  <div class="Alert Alert--info">
                    <strong>Recomendación</strong>

                    <p>Generar un espacio claramente visible que invite a la participación presentando toda la información relativa a las instancias de participación pasadas, presentes (abiertas) y/o futuras. Esta información puede incluir noticias de un evento de participación, actas de reuniones, resultados de una encuesta o consulta, enlaces a las redes sociales en las que participa el organismo, entre otros.</p>
                  </div>

                  <h3 id="2-redes-sociales">2. Redes sociales</h3>
                  <h4 id="como-medio-de-difusi-n-">Como medio de difusión:</h4>
                  <p>Dada la penetración del uso de las redes sociales y la familiaridad con la que los usuarios se manejan en éstas, las redes sociales pueden ser un ámbito donde difundir información que el organismo considere de relevancia. Para ello es importante, explicitar el alcance u objetivo que tendrá dicha red social para que los ciudadanos lo tengan claro.</p>
                  <div class="Alert">
                    <strong>Ejemplo</strong>
                    <p>Este es un espacio del organismo "x" para difundir noticias y novedades sobre...</p>
                  </div>

                  <p>En caso de recibir preguntas o comentarios y que ese canal no sea el previsto para evacuarlos, explicitarlo públicamente para que el resto de los visitantes lo entienda también. Deberán definirse claramente los roles relacionados al presente espacio. El moderador deberá publicar una respuesta estandarizada.</p>
                  <div class="Alert">
                    <strong>Ejemplo</strong>
                    <p>Envía tu consulta o sugerencia a <a href="#">link específico</a>.</p>
                  </div>

                  <h4 id="como-medio-de-interacci-n-con-el-ciudadano-">Como medio de interacción con el ciudadano:</h4>
                  <p>El uso de las redes sociales para este fin se justifica dado que son de masivo alcance y de fácil manejo. Las redes sociales pueden utilizarse para formar debate y relevar opiniones. Se debería explicitar el alcance de dicha red social para que los ciudadanos lo tengan claro.</p>
                  <div class="Alert">
                    <strong>Ejemplo</strong>
                    <p>Este es un espacio para que los ciudadanos opinen sobre el funcionamiento de...</p>
                  </div>

                  <p>Se deberá explicitar el uso que el organismo realizará de esos comentarios u opiniones. Se deberá dar un marco a la participación, acotando el tema y el tiempo de apertura de dicho tema en ese espacio, de forma tal de darle dinamismo.</p>
                  <div class="Alert">
                    <strong>Ejemplo</strong>
                    <p>Esta semana recibimos tus opiniones acerca de "x" tema.</p>
                  </div>

                  <p>Se deberá cerrar ese espacio y realizar una devolución, que puede ser individual o colectiva.</p>
                  <div class="Alert">
                    <strong>Ejemplo</strong>
                    <p>Raconto de propuestas enviadas y comentarios generales "Hubo 10 propuestas vinculadas a..."</p>
                  </div>

                  <p>Deberán definirse claramente los roles relacionados al presente espacio.</p>
                  <h4 id="el-organismo-deber-tener-definido-">El organismo deberá tener definido:</h4>
                  <ul>
                    <li>Quien/es modera/n.</li>
                    <li>En que horarios moderan.</li>
                    <li>Protocolo de respuesta ante agresiones al organismo o terceros, ante acusaciones personales (que violen protección de datos personales).</li>
                    <li>Cómo proceder ante comentarios fuera de tema, muy extensos, repetidos, etc.</li>
                  </ul>
                  <h3 id="3-encuestas-o-votaciones">3. Encuestas o votaciones</h3>
                  <p>Las encuestas o votaciones permiten a los ciudadanos participar y sirven a los organismos como insumo para la toma de decisiones.</p>
                  <div class="Alert Alert--info">
                    <strong>Recomendación</strong>

                    <p>Las encuestas o votaciones deberían realizarse sobre servicios que el organismo brinda, quiere evaluar y con una finalidad bien clara y explícita.</p>

                    <p>Es importante evitar encuestas de opinión general cuyos resultados no afecten al organismo ni el organismo pueda hacer nada con ellos. Se sugiere promover aquellas de cuyos resultados el organismo se pueda nutrir. Ejemplo: "¿Cuál de nuestros trámites te gustaría hacer en línea?"</p>

                    <p>Se deberá cerrar ese espacio y realizar una devolución, que puede ser individual o colectiva.</p>
                  </div>

                  <div class="Alert">
                    <strong>Ejemplo</strong>

                    <p>"El trámite más votado es..."</p>
                  </div>

                  <p>Debemos tener en cuenta que todos estos instrumentos son válidos en la medida en que el organismo se nutra efectivamente de las opiniones o valoraciones de los ciudadanos.</p>
                </div>

